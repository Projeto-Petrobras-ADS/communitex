ALTER TABLE issues ADD CONSTRAINT fk_issue_arquivo FOREIGN KEY (arquivo_id) REFERENCES arquivos(id);
ALTER TABLE atendimentos_denuncia ADD CONSTRAINT fk_atendimento_arquivo FOREIGN KEY (arquivo_id) REFERENCES arquivos(id);

